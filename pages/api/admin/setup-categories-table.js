import { supabase } from '../../../lib/supabaseClient'

export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' })
  }

  try {
    console.log('Setting up family_categories table...')

    // Create the table with proper structure
    const createTableSQL = `
      CREATE TABLE IF NOT EXISTS family_categories (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        family_id UUID NOT NULL REFERENCES families(id) ON DELETE CASCADE,
        category_value VARCHAR(100) NOT NULL,
        category_label VARCHAR(200) NOT NULL,
        category_emoji VARCHAR(10) DEFAULT 'ðŸ“',
        created_at TIMESTAMPTZ DEFAULT NOW(),
        updated_at TIMESTAMPTZ DEFAULT NOW(),
        UNIQUE(family_id, category_value)
      );

      CREATE INDEX IF NOT EXISTS idx_family_categories_family_id ON family_categories(family_id);
      CREATE INDEX IF NOT EXISTS idx_family_categories_value ON family_categories(family_id, category_value);

      ALTER TABLE family_categories ENABLE ROW LEVEL SECURITY;
    `

    // Try to execute the SQL
    const { error: sqlError } = await supabase.rpc('exec_sql', { 
      sql: createTableSQL 
    })

    if (sqlError) {
      console.error('Direct SQL execution failed:', sqlError)
      
      // Try alternative approach - use supabase admin client if available
      throw new Error(`Could not create table: ${sqlError.message}`)
    }

    console.log('Table created successfully')

    // Now try to insert default categories for all existing families
    try {
      // Get all families
      const { data: families, error: familiesError } = await supabase
        .from('families')
        .select('id')

      if (familiesError) {
        console.error('Error fetching families:', familiesError)
      } else if (families && families.length > 0) {
        // Insert default categories for each family
        const defaultCategories = [
          { value: 'memories', label: 'Amintiri', emoji: 'ðŸ’­' },
          { value: 'milestones', label: 'Etape importante', emoji: 'ðŸŽ¯' },
          { value: 'everyday', label: 'Zilnic', emoji: 'â˜€ï¸' },
          { value: 'special', label: 'Special', emoji: 'âœ¨' },
          { value: 'family', label: 'Familie', emoji: 'ðŸ‘¨â€ðŸ‘©â€ðŸ‘§â€ðŸ‘¦' },
          { value: 'play', label: 'JoacÄƒ', emoji: 'ðŸŽ®' },
          { value: 'learning', label: 'ÃŽnvÄƒÈ›are', emoji: 'ðŸ“š' }
        ]

        for (const family of families) {
          const categoriesToInsert = defaultCategories.map(cat => ({
            family_id: family.id,
            category_value: cat.value,
            category_label: cat.label,
            category_emoji: cat.emoji
          }))

          const { error: insertError } = await supabase
            .from('family_categories')
            .upsert(categoriesToInsert, { 
              onConflict: 'family_id,category_value',
              ignoreDuplicates: true 
            })

          if (insertError) {
            console.error(`Error inserting categories for family ${family.id}:`, insertError)
          } else {
            console.log(`Default categories added for family ${family.id}`)
          }
        }
      }
    } catch (insertError) {
      console.error('Error during default category insertion:', insertError)
      // Don't fail the whole operation if default insertion fails
    }

    res.status(200).json({ 
      success: true, 
      message: 'family_categories table setup completed' 
    })
  } catch (error) {
    console.error('Error setting up categories table:', error)
    res.status(500).json({ 
      error: 'Failed to setup categories table', 
      details: error.message 
    })
  }
}